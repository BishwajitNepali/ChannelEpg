package com.missingdrop.channelepg.utils;

public class ShowsInChannel {
	String showName;
	String showLanguage;
	String showLogoLink;
	String showTime;

	public String getShowName() {
		return showName;
	}

	public void setShowName(String showName) {
		this.showName = showName;
	}

	public String getShowLanguage() {
		return showLanguage;
	}

	public void setShowLanguage(String showLanguage) {
		this.showLanguage = showLanguage;
	}

	public String getShowLogoLink() {
		return showLogoLink;
	}

	public void setShowLogoLink(String showLogoLink) {
		this.showLogoLink = showLogoLink;
	}

	public String getShowTime() {
		return showTime;
	}

	public void setShowTime(String showTime) {
		this.showTime = showTime;
	}

}